# Define display_board that accepts a board and prints
# out the current state. 

board = [" "," "," "," "," "," "," "," "," "]



def display_board(board_a)
  puts " #{board_a[0]} | #{board_a[1]} | #{board_a[2]} "
  puts "-----------"
  puts " #{board[3]} | #{board[4]} | #{board[5]} "
  puts "-----------"
  puts " #{board[6]} | #{board[7]} | #{board[8]} "
 
end
display_board(board)


 
 